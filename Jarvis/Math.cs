using System;

namespace Jarvis
{
	public class Math
	{
		public Math ()
		{
			public float add(float val1, float val2)
			{
				float result = val1 + val2
				return result;
			}

			public float subtract(float val1, float val2)
			{
				float result = val1 + val2;
				return result;
			}

			public float divide(float val1, float val2)
			{
				float result = val1/val2
				return result;
			}

			public float multiply(float val1, float val2)
			{
				float result = val1 * val2 
				return result;
			}
		}
	}
}